package Operadores;

public class Oper_Comparacion {
    static void main(String[] args) {
        int valor1 = 90, valor2 = 50; //Esto se conoce como programación lineal.

        // Igualdad
        System.out.println(valor1 == valor2);

        // Distinto a...
        System.out.println(valor1 != valor2);

        // Mayor que ...
        System.out.println(valor1 > valor2);

        // Menor que ...
        System.out.println(valor1 < valor2);

        // Menor o igual que...
        System.out.println(valor1 <= valor2);

        // Mayor o igual que...
        System.out.println(valor1 >= valor2);
    }
}
